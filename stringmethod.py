str="nothing is possible in this curse world"
#capitalize method
x=str.capitalize()
print(x)

#casefold method
y=str.casefold()
print(y)

#center method
a=str.center(200)
print(a)

#count method
b=str.count("curse")
print(b)

#encode method
c=str.encode()
print(c)

#lower method
d=str.lower()
print(d)

#upper method
u=str.upper()
print(u)
